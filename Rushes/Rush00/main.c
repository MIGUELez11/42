#include <unistd.h>

int	main(void)
{
	int n;
	int i;
	n = 5;
	i = 1;
	while(i <= n)
	{	
		write(1, "*" ,1); 
		i++;
	}	
		i = 1;
		while(i < n)		
		{
		write(1, "\n*" ,2);
		i++;
		}
		
		return(0);
}	
