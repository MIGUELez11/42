void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int aux[size];

	i = -1;
	while (++i < size)
	{
		aux[i] = *(tab + i);
	}
	while (i >= 0)
	{
		*(tab + size - 1 - i) = aux[i];
		i--;
	}
}
