int	ft_str_is_uppercase(char *str)
{
	int i;
	int upper;

	i = 0;
	while (*(str + i) != 0)
	{
		upper = *(str + i) >= 'A' && *(str + i) <= 'Z';
		if (upper == 0)
			return (0);
		i++;
	}
	return (1);
}
